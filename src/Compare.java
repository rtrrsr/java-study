    //创建类 比较运算符
public class Compare {
    //主方法 权限修饰类 静态修饰类 返回值修饰类
    public static void main(String[] args) throws Exception {
        int number_1 = 5;          //定义整形局部变量
        int number_2 = 7;
        System.out.println("number_1 > number_2的返回值：" + (number_1 > number_2));
        System.out.println("number_1 < number_2的返回值：" + (number_1 < number_2));
        System.out.println("number_1 == number_2的返回值：" + (number_1 == number_2));
        System.out.println("number_1 != number_2的返回值：" + (number_1 != number_2));
    }
}