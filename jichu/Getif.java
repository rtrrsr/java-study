package jichu;
  //创建类 简单的if条件语句
public class Getif{
    //主方法 权限修饰符 静态修饰符 返回值修饰符
    public static void main(String[] args) throws Exception {
        int x = 10;
        int y = 20;
        if(x > y){
            System.out.println("变量x大于变量y");
        }
        if(x < y){
            System.out.println("变量x小于变量y");
        }
    }
}